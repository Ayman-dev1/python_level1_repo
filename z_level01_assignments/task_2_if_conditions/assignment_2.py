student_grade = input('Enter your Grade: ')

if student_grade == 'A':
    print('Excellent')
elif student_grade == 'B':
    print('V.Good')
elif student_grade == 'C':
    print('Good')
elif student_grade == 'D':
    print('Pass')
elif student_grade == 'E':
    print('Fail')
else:
    print('Invalid Grade')